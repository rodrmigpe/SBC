    private void btProximoMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_btProximoMouseClicked
        // TODO add your handling code here:
    if (txtIdade.getText().trim().isEmpty()) {     
        JOptionPane.showMessageDialog(null,"Por favor insira a sua idade!","Erro",JOptionPane.ERROR_MESSAGE);
        txtIdade.requestFocus();
        return;
    } 
    
    if(!txtIdade.getText().matches("[0-9]+")) {
        JOptionPane.showMessageDialog(null,"Por favor insira uma idade correta!","Erro",JOptionPane.ERROR_MESSAGE);
        txtIdade.requestFocus();
        return;
    }
    
    dispose();
    Regiao regiao = new Regiao();
    regiao.setLocationRelativeTo(null);
    regiao.setVisible(true);
        
    }//GEN-LAST:event_btProximoMouseClicked

