    private void btProximoMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_btProximoMouseClicked
        // TODO add your handling code here:
    if (txtNome.getText().trim().isEmpty()) {        
        JOptionPane.showMessageDialog(null,"Por favor insira o seu nome!","Erro",JOptionPane.ERROR_MESSAGE);
        txtNome.requestFocus();
        return;
    }
    
    if (txtNome.getText().matches(".*\\d.*")) {
        JOptionPane.showMessageDialog(null,"Por favor insira um nome correto!","Erro",JOptionPane.ERROR_MESSAGE);
        txtNome.requestFocus();
        return;
    }
    
    dispose();
    Idade idade = new Idade();
    idade.setLocationRelativeTo(null);
    idade.setVisible(true);        
    }//GEN-LAST:event_btProximoMouseClicked

